    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int column = 0;
        int row = jTable1.getSelectedRow();
        if (jTable1.getRowSorter() != null) {
            row = jTable1.getRowSorter().convertRowIndexToModel(row);
        }
        String value = jTable1.getModel().getValueAt(row, column).toString();

        setData();
        sql = "SELECT * FROM purchase WHERE SaleID = '" + value + "'";
        DBconnect.executeQ(sql);
        try {
            DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
            tableModel.setRowCount(0);
            cmbProductID.removeAllItems();
            while (DBconnect.rs.next()) {
                tableModel.addRow(new Object[]{DBconnect.rs.getString(2).toString(),
                    DBconnect.rs.getString(3).toString(),
                    DBconnect.rs.getString(4).toString(),
                    DBconnect.rs.getString(5).toString()});
                cmbProductID.addItem(DBconnect.rs.getString(2).toString());
            }
            sql = "SELECT CustomerID FROM sale WHERE saleID ='" + value + "'";
            ResultSet rs = DBconnect.st.executeQuery(sql);
            if (rs.isBeforeFirst()) {
                rs.first();
                txtSalesID.setText(value);
                txtCustomerID.setText(rs.getString(1));
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        btnDeletePrd.setEnabled(true);
        btnChange.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

