    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        setData();
        //sl.setQnt(Math.abs(Integer.parseInt(txtQuantity.getText())));
        int NewQnt = Integer.parseInt(txtQuantity.getText());
        double total = price() * NewQnt;


        int AddedQnt = (int) (Avq() + qnt());
        if (NewQnt > AddedQnt) {
            JOptionPane.showMessageDialog(null, "Unavailable quantity");

        } else {
            int NewAvq = AddedQnt - NewQnt;
            sql = "UPDATE products SET AvailableQuantity = " + NewAvq + " WHERE ProductID = '" + st.getProductID() + "'";
            DBconnect.updateQ(sql);
            sql = "UPDATE purchase SET Quantity = " + NewQnt + ", Total= " + total + " WHERE SaleID ='" + sl.getSaleID() + "' AND ProductID ='" + st.getProductID() + "'";
            DBconnect.updateQ(sql, "Changed");
            viewPurchase();
            txtQuantity.setText("");
        }

    }//GEN-LAST:event_btnChangeActionPerformed

