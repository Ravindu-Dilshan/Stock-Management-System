    private void btnCustomerSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerSaveActionPerformed
        if (txtCustomerID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Customer ID");
        } else if (txtCustomerName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Customer Name");
        } else if (txtCustomerAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Customer Address");
        } else if (txtCustomerTel.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Customer Telephone");
        } else if (txtCustomerDate.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Customer Entered Date");
        } else {
            if (txtCustomerTel.getText().length() < 10) {
                JOptionPane.showMessageDialog(this, "Phone Number Invalid");
            } else if (txtCustomerTel.getText().length() > 10) {
                JOptionPane.showMessageDialog(this, "Phone Number Invalid");
            } else {
                /*---------------------------------------------------*/
                CustomerData c1 = new CustomerData();
                c1.setCustomerID(txtCustomerID.getText());
                c1.setCustomerName(txtCustomerName.getText());
                c1.setAddress(txtCustomerAddress.getText());
                c1.setTelephone(Math.abs(Integer.parseInt(txtCustomerTel.getText())));
                c1.setDate(txtCustomerDate.getText());

                sql = "INSERT INTO customer VALUES ('" + c1.getCustomerID() + "','" + c1.getCustomerName() + "','" + c1.getAddress() + "'," + c1.getTelephone() + ",'" + c1.getDate() + "')";
                DBconnect.updateQ(sql, "Saved");
                LastRecord();
                clear();
            }
        }
    }//GEN-LAST:event_btnCustomerSaveActionPerformed

