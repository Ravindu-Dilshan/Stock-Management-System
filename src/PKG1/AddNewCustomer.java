    private void Calender() {
        Calendar c = new GregorianCalendar();
        int year, month, date;
        date = c.get(Calendar.DATE);
        month = c.get(Calendar.MONTH);
        year = c.get(Calendar.YEAR);
        txtCustomerDate.setText(year + "/" + (month + 1) + "/" + date);
    }

    private void LastRecord() {
        sql = "SELECT customer.CustomerID FROM customer ORDER By customer.CustomerID DESC LIMIT 1";
        DBconnect.executeQ(sql);
        try {
            while (DBconnect.rs.next()) {
                lblLast.setText("Last Record ID : " + DBconnect.rs.getString(1));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void clear() {
        txtCustomerAddress.setText("");
        Calender();
        txtCustomerID.setText("");
        txtCustomerName.setText("");
        txtCustomerTel.setText("0");
    }

